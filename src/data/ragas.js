// Helper functions
export function getRaga(key) {
  const normalizedKey = key.toLowerCase().replace(/[\s-]/g, '');
  return ragas[normalizedKey] || ragas[key] || Object.values(ragas).find(r =>
    r.name.toLowerCase().replace(/[\s-]/g, '') === normalizedKey
  ) || null;
}

export function getAllRagas() {
  return { ...ragas };
}

export function getRagaNames() {
  return Object.values(ragas).map(r => r.name);
}

export function getRagasByTime(timeKeyword) {
  const keyword = timeKeyword.toLowerCase();
  return Object.entries(ragas)
    .filter(([_, r]) => r.time.toLowerCase().includes(keyword))
    .map(([id, r]) => ({ id, ...r }));
}

export function getRagasByMood(mood) {
  const moodLower = mood.toLowerCase();
  return Object.entries(ragas)
    .filter(([_, r]) => r.mood.some(m => m.toLowerCase().includes(moodLower)))
    .map(([id, r]) => ({ id, ...r }));
}

export function getRagasByThaat(thaat) {
  const thaatLower = thaat.toLowerCase();
  return Object.entries(ragas)
    .filter(([_, r]) => r.thaat.toLowerCase() === thaatLower)
    .map(([id, r]) => ({ id, ...r }));
}

export function getRagasBySeason(season) {
  const seasonLower = season.toLowerCase();
  return Object.entries(ragas)
    .filter(([_, r]) => r.season?.toLowerCase() === seasonLower)
    .map(([id, r]) => ({ id, ...r }));
}

export default ragas;
